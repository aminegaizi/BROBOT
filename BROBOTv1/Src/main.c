/*
 * Generate delay in micro seconds for Ultrasonic sensor handling
 */
void usDelay(uint32_t uSec)
{
	if(uSec < 2) uSec = 2;
		TIM4->ARR = uSec - 1; 	/*sets the value in the auto-reload register*/
		TIM4->EGR = 1; 			/*Re-initialises the timer*/
		TIM4->SR &= ~1; 		//Resets the flag
		TIM4->CR1 |= 1; 		//Enables the counter
		while((TIM4->SR&0x0001) != 1);
		TIM4->SR &= ~(0x0001);
}

/*
 * Handling of the Ultrasonic sensor
 * Measurement and calculation of the distance
 * Send data through UART
 */
void UltrasonicSensorHandling()
{
	uint32_t numTicks = 0;
	//Set TRIG to LOW for few uSec
	HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, GPIO_PIN_RESET);
	usDelay(3);

	//*** START Ultrasonic measure routine ***//
	//1. Output 10 usec TRIG
	HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, GPIO_PIN_SET);
	usDelay(10);
	HAL_GPIO_WritePin(TRIG_GPIO_Port, TRIG_Pin, GPIO_PIN_RESET);

	//2. Wait for ECHO pin rising edge
	while(HAL_GPIO_ReadPin(ECHO_GPIO_Port, ECHO_Pin) == GPIO_PIN_RESET);

	//3. Start measuring ECHO pulse width in usec
	numTicks = 0;
	while(HAL_GPIO_ReadPin(ECHO_GPIO_Port, ECHO_Pin) == GPIO_PIN_SET)
	{
	numTicks++;
	usDelay(2); //2.8usec
	};

	//4. Estimate distance in cm
	distance = (numTicks + 0.0f)*2.8*speedOfSound;

	//5. Print to UART terminal for debugging
	sprintf(uartBuf, "Distance (cm)  = %0.1f\r\n", distance);

	HAL_UART_Transmit(&huart1, (uint8_t *)uartBuf, strlen(uartBuf), 90);
	HAL_UART_Transmit(&huart2, (uint8_t *)uartBuf, strlen(uartBuf), 90);
}

